let num1= parseInt(prompt("Ingrese un primer numero "));
let num2= parseInt(prompt("Ingrese un segundo numero "));
if(num1>num2){
    document.write("El primer numero ingresado " + num1 + " es mayor");
}
else if(num2>num1){
    document.write("El segundo numero ingresado " + num2 + " es mayor");
}
else document.write("los numeros son iguales");

